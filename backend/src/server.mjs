// Enhanced OpenAI client with better error handling
let openaiClient = null;

function initializeOpenAI() {
	try {
		const apiKey = process.env.OPENAI_API_KEY;
		if (!apiKey) {
			console.warn("‚ö†Ô∏è  OPENAI_API_KEY not found - chat functionality will be disabled");
			return null;
		}
		
		openaiClient = new OpenAI({ 
			apiKey,
			timeout: 30000, // 30 second timeout
			maxRetries: 3
		});
		
		console.log("‚úÖ OpenAI client initialized successfully");
		return openaiClient;
	} catch (error) {
		console.error("‚ùå Failed to initialize OpenAI client:", error.message);
		return null;
	}
}

// Initialize OpenAI on startup
initializeOpenAI();

function getOpenAI() {
	if (!openaiClient) {
		throw new Error("OpenAI client not initialized - check OPENAI_API_KEY");
	}
	return openaiClient;
}

